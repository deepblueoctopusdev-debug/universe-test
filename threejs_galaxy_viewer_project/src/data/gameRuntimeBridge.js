function clamp(value, min, max) {
  return Math.max(min, Math.min(max, value));
}

function parseCoordString(value) {
  const parts = String(value).split(":").map((entry) => Number.parseInt(entry, 10) || 0);
  return {
    galaxy: parts[0] || 1,
    sector: parts[1] || 1,
    system: parts[2] || 1,
    planet: parts[3] || 0,
  };
}

function toViewerPosition(coords) {
  const galaxyOffset = (coords.galaxy - 1) * 620;
  const sectorOffset = coords.sector * 9;
  const systemOffset = coords.system * 1.6;
  return {
    x: galaxyOffset + sectorOffset + systemOffset,
    y: ((coords.system % 9) - 4) * 10 + (coords.galaxy - 1) * 24,
    z: ((coords.sector % 36) - 18) * 32 + coords.system * 0.9,
  };
}

function createMoon(id, name, biome, orbitRadius, size) {
  return {
    id,
    name,
    size,
    orbitRadius,
    orbitSpeed: 0.008,
    orbitAngle: orbitRadius * 0.25,
    biome,
  };
}

function createPlanet(planet) {
  return {
    id: planet.id,
    name: planet.name,
    size: planet.size,
    orbitRadius: planet.orbitRadius,
    orbitSpeed: planet.orbitSpeed,
    orbitAngle: planet.orbitAngle,
    color: planet.color,
    biome: planet.biome,
    worldClass: planet.worldClass,
    habitability: planet.habitability,
    resourceValue: planet.resourceValue,
    moons: planet.moons || [],
  };
}

function createSystem(options) {
  const metrics = options.metrics;
  const planets = options.planets.map(createPlanet);
  return {
    id: options.id,
    name: options.name,
    position: toViewerPosition(options.coordinates),
    arm: (options.coordinates.galaxy - 1) % 5,
    sector: `G${options.coordinates.galaxy}-S${String(options.coordinates.sector).padStart(3, "0")}`,
    starSize: options.starSize,
    temperature: options.temperature,
    planets,
    stations: options.stations,
    interstellarObjects: options.interstellarObjects,
    asteroidBelts: options.asteroidBelts,
    metrics,
    alliance: options.alliance,
    hasJumpGate: Boolean(options.hasJumpGate),
    hasAnomaly: Boolean(options.hasAnomaly),
    description: options.description,
    primaryPlanetId: planets[0]?.id || null,
    origin: "main-game-bridge",
    sourceModules: options.sourceModules,
  };
}

