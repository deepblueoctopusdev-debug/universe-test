export function getMenuById(menuId) {
  return MENU_DEFINITIONS.find((menu) => menu.id === menuId) || MENU_DEFINITIONS[0];
}

export function getPageById(pageId) {
  for (const menu of MENU_DEFINITIONS) {
    const page = menu.pages.find((entry) => entry.id === pageId);
    if (page) {
      return { ...page, menuId: menu.id, menuLabel: menu.label };
    }
  }

  const fallbackMenu = MENU_DEFINITIONS[0];
  return { ...fallbackMenu.pages[0], menuId: fallbackMenu.id, menuLabel: fallbackMenu.label };
}

export function getDefaultPage(menuId) {
  return getMenuById(menuId).pages[0];
}
