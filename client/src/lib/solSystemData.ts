export interface CelestialCoordinates {
  galaxy: number;
  sector: number;
  system: number;
  orbit: number;
  index: number;
}

export interface CelestialBody {
  id: string;
  name: string;
  type: 'star' | 'planet' | 'dwarf_planet' | 'moon' | 'asteroid' | 'comet';
  parentId: string | null;
  coordinates: CelestialCoordinates;
  coordinateString: string;
  properties: {
    mass?: number;
    radius?: number;
    orbitalPeriod?: number;
    rotationPeriod?: number;
    distanceFromParent?: number;
    surfaceGravity?: number;
    meanTemperature?: number;
    atmosphere?: string[];
    composition?: string[];
    rings?: boolean;
    habitable?: boolean;
    description?: string;
  };
}

export interface StarSystem {
  id: string;
  name: string;
  coordinates: CelestialCoordinates;
  coordinateString: string;
  starType: string;
  bodies: CelestialBody[];
}

export interface Galaxy {
  id: string;
  name: string;
  type: 'spiral' | 'elliptical' | 'irregular' | 'lenticular';
  size: 'dwarf' | 'small' | 'medium' | 'large' | 'giant';
  starCount: number;
  diameter: number;
  coordinates: { x: number; y: number; z: number };
}

export function formatCoordinates(coords: CelestialCoordinates): string {
  return `${coords.galaxy}:${coords.sector}:${coords.system}:${coords.orbit}:${coords.index}`;
}

export function parseCoordinates(coordString: string): CelestialCoordinates {
  const parts = coordString.split(':').map(Number);
  return {
    galaxy: parts[0] || 0,
    sector: parts[1] || 0,
    system: parts[2] || 0,
    orbit: parts[3] || 0,
    index: parts[4] || 0,
  };
}

