export function getSolSystemPlanets(): CelestialBody[] {
  return SOL_SYSTEM.bodies.filter(b => b.type === 'planet');
}

export function getSolSystemMoons(): CelestialBody[] {
  return SOL_SYSTEM.bodies.filter(b => b.type === 'moon');
}

export function getSolSystemDwarfPlanets(): CelestialBody[] {
  return SOL_SYSTEM.bodies.filter(b => b.type === 'dwarf_planet');
}

export function getCelestialBodyById(id: string): CelestialBody | undefined {
  return SOL_SYSTEM.bodies.find(b => b.id === id);
}

export function getMoonsOfPlanet(planetId: string): CelestialBody[] {
  return SOL_SYSTEM.bodies.filter(b => b.parentId === planetId && b.type === 'moon');
}

export function getBodyByCoordinates(coordString: string): CelestialBody | undefined {
  return SOL_SYSTEM.bodies.find(b => b.coordinateString === coordString);
}
