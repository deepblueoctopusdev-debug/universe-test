export function getStarSystemById(id: string): StarSystem | undefined {
  return ALL_STAR_SYSTEMS.find(s => s.id === id);
}

export function getStarSystemByCoords(coordStr: string): StarSystem | undefined {
  return ALL_STAR_SYSTEMS.find(s => s.coordinateString === coordStr);
}

export function getStarSystemByName(name: string): StarSystem | undefined {
  return ALL_STAR_SYSTEMS.find(s => s.name.toLowerCase().includes(name.toLowerCase()));
}

export function getBodyInSystem(systemId: string, bodyId: string): CelestialBody | undefined {
  const system = getStarSystemById(systemId);
  if (!system) return undefined;
  return system.bodies.find(b => b.id === bodyId);
}

export function getAllBodies(): CelestialBody[] {
  return ALL_STAR_SYSTEMS.flatMap(s => s.bodies);
}

export function getBodiesByType(type: string): CelestialBody[] {
  return getAllBodies().filter(b => b.type === type);
}

