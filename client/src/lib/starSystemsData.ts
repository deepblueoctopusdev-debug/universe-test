function makeCoords(galaxy: number, sector: number, system: number, orbit: number = 0, index: number = 0): CelestialCoordinates {
  return { galaxy, sector, system, orbit, index };
}

function coordStr(coords: CelestialCoordinates): string {
  return `${coords.galaxy}:${coords.sector}:${coords.system}:${coords.orbit}:${coords.index}`;
}

